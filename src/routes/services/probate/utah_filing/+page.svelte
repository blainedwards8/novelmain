      <h2>STEP 1: Gather necessary information/paperwork</h2>
      <div>
        Important Resources:
        <ul class="mb-5">
          <li><a href="#">Novel Law Group Probate Intake Form</a></li>
        </ul>
      </div>

      <p>
        The first information you need to gather is whether you even need to
        file for probate. We provide a <a href="#">useful guide</a> to making this
        determination.
      </p>

      <p>
        If you made it here, you must have determined that filing for probate is
        necessary. Like many things in life, it helps to start with a plan. When
        our firm does a probate, our plan starts with an intake form so that we
        can start to gather information necessary to accomplish the probate. We
        provide our <a href="#">form</a>
        for your use. This will be a life saver! Don't skimp on filling it out and
        keeping it up to date with new information. You will find yourself referencing
        it over and over. Whenever you make a phonecall or have an in person meeting,
        have the form close by for reference.
      </p>
      <p>
        The intake form contains a checklist of documents that you will need to
        collect to accomplish the probate. Some are needed immediately and
        others can be gathered after the start of the probate. It helps to keep
        a binder of all these documents. In the firm, we gather most documents
        digitally. <a href="https://drive.google.com">Google Drive</a> is very helpful
        for storing documents and sharing them with others. However, there are times
        when you will need the hard original document.
      </p>
      <p>
        Your plan starts off with gathering information about the estate.
        Depending on the documents that you collect, you will handle the next
        steps differently.
      </p>
      <h3>Death Certificate</h3>
      <p>
        If you haven't ordered death certificates, do it now! This will be the
        hang up if you don't. Normally, you can order the death certificate from
        the mortuary. A big question that gets asked is "how many certificated
        do I need?" Get between 5-7 certificates. You probably won't need that
        many. But in case you do, it is easy to over order and have too many
        instead of not having enough and needing to reorder.
      </p>

      <p>
        If necessary, you can always obtain a copy of the death certificate from
        <a href="https://vitalrecords.utah.gov/">Vital Records</a>.
      </p>

      <p>
        You will need at least one death certificate to complete the filing with
        the court. Everywhere you go, you might be asked to show a death
        certificate.
      </p>

      <p class="alert">
        You must be a parent, sibling or descendent of the decedent to order a
        death certificate.
      </p>

      <h3>Last Will and Testament</h3>
      <p>
        This is a document that you may not find, but you need to do a search
        for. There is no law that states that a person must have a will when
        they die and most people do not. Depending on whether a has a will when
        they die or not determines testacy. When a person dies with a will that
        person dies testate. When a person dies without a will or their will is
        invalid, they die intestate.
      </p>

      <p class="info"><a href="#">What if the decedant had a trust?</a></p>

      <p class="alert">
        Concealing the existance of a will can result in criminal charges. If
        the Will exists and probate is necessary, the Will must be probated.
      </p>

      <p>
        The Will is an important document because it instructs the court how the
        last desires of the decedent. Please note, however, the Will is not
        binding on the court (meaning the court does not have to follow the
        Will). The court has broad power to make decisions concerning the
        estate, but it is done under the law of justice. In other words, the
        judge of your probate has the final say unless you want to appeal his
        decision.
      </p>
      <div>
        The Will provides information on several important topics, some of which
        are:
        <ul style="list-style-type: disc" class="ml-5 mb-5">
          <li>Who the beneficiaries are.</li>
          <li>Who the decedent wanted to administer his/her estate.</li>
          <li>How the property in his estate should be divided.</li>
        </ul>
      </div>
      <p>
        As mentioned above, the court uses the Will to make these decisions. But
        if there is not a will, the court must fill in the gaps using its powers
        of justice.
      </p>
    </div>

    <div id="beneficaries">
      <h2>STEP 2: Identify Beneficiaries</h2>
      <div>
        Important Resources:
        <ul class="mb-5 ml-5" style="list-style-type: disc">
          <li>The Will (if it exists)</li>
          <li>
            <a
              href="https://le.utah.gov/xcode/Title75/Chapter3/75-3-P7.html?v=C75-3-P7_1800010118000101"
              >Utah Code - Duties and Power of Personal Representatives</a
            >
          </li>
        </ul>
      </div>
      <div>
        The remaining steps must be viewed in light of whether the decedent died
        testate or intestate (with or without a will). Since the Will is the
        guiding document for the probate, which the court will use to make
        decisions. Each of the remaining steps of the guide will be broken into
        two parts (testate or intestate). You do not need to follow both parts.
        Only the part you that will fit your probate.

        <h4>Intestate</h4>
        <p>
          Without a will, you will need to determine the beneficiaries of the
          estate. Utah, like most states, follows a system of priorities. This
          system emphasizes the current spouse and children of the decedent.
          While it may seem very cut and dry, there are many scenarios that
          could effect this.
        </p>
        <p>
          These same priorities also apply when the court must pick a personal
          represenative (which we will discuss later).
        </p>

        <p>The levels of priority are:</p>

        <ul class="ml-5 mb-5" style="list-style:disc">
          <li>Spouse</li>
          <li>Children</li>
          <li>Descendants</li>
          <li>Parents</li>
          <li>Siblings</li>
          <li>Grandparents</li>
          <li>Aunts/Uncles</li>
          <li>Cousins</li>
          <li>So on . . .</li>
        </ul>

        <p>
          All of these do not need to be listed. In fact, usually the first
          three are all you will need to identify. But if the first three do not
          exist, then you must find a group or class that has at least one
          member. In other words, if the decedent did not have a spouse,
          children, or any descendants that were alive during the time of death,
          you look at the parents. If the parents are not alive, you look at the
          siblings, and so on. The list is not exhaustive, meaning we can always
          go on until we find someone that should inherit the property of the
          estate. In the event that no one can be found, the property will
          "escheat" to the state, where the state will assume control.
        </p>

        <p>
          It would be impossible to detail every scenario that might exist in a
          probate with respect to beneficiaries. Know that if the decedent is
          married, the spouse is a beneficiary. Any child that survived the
          decedent by at least 24 hours could be a beneficiary. The first group
          of descendants that has a living descendant of a predeceased child of
          the decedent could a beneficiary (that is a mouthful). Let me give an
          example.
        </p>
        <div class="example mb-5">
          <h3 class="mb-4">Example</h3>
          A husband and wife have three children (all adults). Husband passes away.
          Prior to the passing, the first of the three children died. The child that
          died had two children that are alive.
          <p><b>Who are the potential beneficaries?</b></p>
          <ul class="ml-5" style="list-style-type:disc">
            <li>Wife</li>
            <li>Two children that survived</li>
            <li>Two grandchildren of the child that passed away</li>
          </ul>
        </div>

        <p>
          In our example, we stated "potential beneficiaries". Not everyone is
          necessarily a beneficiary. It all depends on the relationship between
          the spouse and the children. If the spouse is the parent of the
          children, the children and any other descendent is not a beneficiary.
          You can cut it off right there. However, if the spouse is not the
          parent of the children, the children that survived the descedent
          become beneficaries. The process keeps going until each living family
          of a child of the descendant is represented in some way. However, not
          every descendant is necessarily a beneficiary.
        </p>

        <p>
          At this point in time, we are not concerned with what the
          beneficiaries might get. We just want to know who the beneficaries are
          so we can get ready for filing the probate.
        </p>

        <p class="alert">
          The personal representative must perform a thorough search to
          determine the beneficiaries. Intentionally leaving off a beneficiary
          from a filing may have really bad consequences for the personal
          representative.
        </p>

        <p>
          A scenario that often happens is husband and wife have children.
          However, one of the spouses might have a child from a previous
          marriage or outside of marriage. As sometimes happens, an illigitimate
          child is not welcomed by the family and the family may not include the
          child as a beneficary in a probate. <b
            >Don't be the one that refuses to add this person as a beneficiary
            -- you will not gain favor with the court (and that is a bad thing).</b
          >
        </p>

        <h4>Testate</h4>
        <p>
          If there is a will, the will contains the information on who is a
          beneficiary (with some exceptions).
        </p>

        <p>
          Beneficaries are often identified as groups(e.g. "my children"), and
          sometimes the individuals in the group will be specifically named
          (e.g. "my children are . . . "). When groups are mentioned in the
          will, the person that will be applying to become the personal
          representative must identify those individuals that would be in the
          group and any potential beneficiaries that could end up being in that
          group.
        </p>

        <div class="example mb-5">
          <h3 class="mb-4">Example</h3>
          A widowed man makes a gift in his will "to all my grandchildren". At the
          time of his passing, the man has five children, each of which has children.
          Each of the grandchildren have a vested interest in the estate because
          they are one of the grandchildren. However, what if another grandchild
          is born after the death of the man? Does that grandchild have a vested
          interest?
        </div>

        <p>
          The example above may seem like a nuance, but unfortunately when money
          is involved, these things matter. The short answer to the above
          questions is yes. Any unborn grandchild is a member of the group and
          therefore, a beneficiary. When you file for probate, you are not
          including groups as the beneficiary though (nor are you including
          unborn children). You are only including those who have an interest in
          the estate (which might include individuals who have a condititional
          interest in the estate).
        </p>

        <p>
          Interests in an estate may be vested or non-vested. A vested interest
          occurs when the individual has to do nothing in order to receive the
          interest from the estate. A non-vested interest occurs when the
          individual must do something in order to take (e.g. "to pay for
          college expenses" or "if he goes to college"). If the person is named
          in the will, the person is a beneficary regardless if the person has a
          vested interest.
        </p>

        <p>
          There are some occasions when a person not named in a will has an
          interest in the estate. These interests are created by statute. An
          example of this is when a spouse is not included in a will. The spouse
          might have an interest in the will regardless.
        </p>
      </div>
    </div>

    <div id="pr">
      <h2>STEP 3: Determine who should be the personal representative</h2>
      <ul class="mb-5 ml-5" style="list-style-type: disc">
        <li>The Will (if it exists)</li>
        <li>
          <a
            href="https://le.utah.gov/xcode/Title75/Chapter3/75-3-P7.html?v=C75-3-P7_1800010118000101"
            >Utah Code - Duties and Power of Personal Representatives</a
          >
        </li>
      </ul>
      <p>
        Let's face it, being a personal representative is not a sought after
        position. It is hard! With that being said, you may still end up
        fighting over who should be the personal represenative.
      </p>

      <p>
        Personal represenatives are appointed by a judge during the probate
        process. Hence, the persona representative position is not assumed. The
        judge will use the law to determine who the personal representative
        should be.
      </p>

      <div>
        In order to be named a personal represenative, the person must be
        qualified to become such. A person is disqualified if:
        <ul>
          <li>the person is younger than 21 years of age; and</li>
          <li>
            the court found the person to be unsuitable in a prior proceeding.
          </li>
        </ul>
      </div>

      <div>
        When appointing the personal representatitve, the court uses a system of
        priority. The priority goes as follows:
        <ul class="ml-5 mb-5" style="list-style-type: disc">
          <li>Person named in the will to become personal represenative</li>
          <li>Surviving Spouse that is a beneficary of the estate</li>
          <li>Other devisee of the decedent</li>
          <li>Surving spouse that is not a beneficiary of the will</li>
          <li>Other heirs of the decedent</li>
          <li>
            45 days after the death of the decedent, any creditor may file
          </li>
        </ul>
      </div>
    </div>

    <p class="alert">
      Just because a person has priority and qualified to become the personal
      representative, a person may contest that person's ability to become the
      personal represenative.
    </p>

